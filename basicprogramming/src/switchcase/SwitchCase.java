package switchcase;

public class SwitchCase {
	public static void main(String[] args) {
		int i=2;
		switch(i)
		{
		case 0:
			System.out.println("value is 2");
			break;
		case 1:
			System.out.println("value is 4");
			break;
		
		case 2:
			System.out.println("value is 6");
			break;
	
			default:
				System.out.println("no value");
		}
	}
}

