package switchcase;

public class SwitchCaseUsingString {
	public static void main(String[] args) {
		String i="four";
				switch (i)
				{
	case "zero":
		System.out.println("value is 0");
		break;
	case "one":
		System.out.println("value is 1");
		break;
	
	case "two":
		System.out.println("value is 2");
		break;

		default:
			System.out.println("no value");
	}

}
}
