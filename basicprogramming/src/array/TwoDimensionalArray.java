package array;

public class TwoDimensionalArray {
	public static void main(String[] args) {
		String x="Jhon Watt";
		System.out.println("hello"+x); // + is used for concatination.
		System.out.println(x.toUpperCase()); //converts string to uppercase
		System.out.println(x.substring(2)); //prints from x[2] to x[8]
		System.out.println(x.substring(2,8));//prints from x[2]to x[7]
		System.out.println(x.charAt(3)); //x[3] i,e n will be printed
	}

}
