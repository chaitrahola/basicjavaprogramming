package continuestatement;

public class Continue {
	  
		public static void main(String[] args) {  
		    for(int i=1;i<=15;i++)
		    {  
		        if(i==3)
		        {  
		            continue;  
		// loop continues even after count i=3
		        }  
		        System.out.println(i);  
		    }  
		}  
		}  


